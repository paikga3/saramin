package beans;

public class AdviceBean {
	// 상담 내용 관련 빈즈

	private int advice_no; // 상담 내용 관리 번호 PK
	private int member_no; // 신청 회원 정보
	private String advice_type; // 상담 종류
	private String advice_content; // 상담 신청 내용
	private String advice_date; // 상담 신청 날짜
	private String advice_redate; // 답변 날짜
	private int advice_count; // 조회수
	private int advice_re_count; // 답글 수
	// 조인
	private String member_email; // 회원 메일을 member_no로 조인
	private String member_name; // 회원 이름을 member_no로 조인

	public int getAdvice_no() {
		return advice_no;
	}

	public void setAdvice_no(int advice_no) {
		this.advice_no = advice_no;
	}

	public int getMember_no() {
		return member_no;
	}

	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}

	public String getAdvice_type() {
		return advice_type;
	}

	public void setAdvice_type(String advice_type) {
		this.advice_type = advice_type;
	}

	public String getAdvice_content() {
		return advice_content;
	}

	public void setAdvice_content(String advice_content) {
		this.advice_content = advice_content;
	}

	public String getAdvice_date() {
		return advice_date;
	}

	public void setAdvice_date(String advice_date) {
		this.advice_date = advice_date;
	}

	public String getAdvice_redate() {
		return advice_redate;
	}

	public void setAdvice_redate(String advice_redate) {
		this.advice_redate = advice_redate;
	}

	public int getAdvice_count() {
		return advice_count;
	}

	public void setAdvice_count(int advice_count) {
		this.advice_count = advice_count;
	}

	public int getAdvice_re_count() {
		return advice_re_count;
	}

	public void setAdvice_re_count(int advice_re_count) {
		this.advice_re_count = advice_re_count;
	}

	public String getMember_email() {
		return member_email;
	}

	public void setMember_email(String member_email) {
		this.member_email = member_email;
	}

	public String getMember_name() {
		return member_name;
	}

	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}

}